package com.zuci.zuciapp.ui.transaction;

public class AddCoinsRateModel {
    int RegistrationId;
    int AudioCallCoins;
    int VideoCallCoins;
    int ImageCoins;
    int VideoCoins;
    int LiveCallCoins;

    public AddCoinsRateModel() {
    }

    public int getRegistrationId() {
        return RegistrationId;
    }

    public void setRegistrationId(int registrationId) {
        RegistrationId = registrationId;
    }

    public int getAudioCallCoins() {
        return AudioCallCoins;
    }

    public void setAudioCallCoins(int audioCallCoins) {
        AudioCallCoins = audioCallCoins;
    }

    public int getVideoCallCoins() {
        return VideoCallCoins;
    }

    public void setVideoCallCoins(int videoCallCoins) {
        VideoCallCoins = videoCallCoins;
    }

    public int getImageCoins() {
        return ImageCoins;
    }

    public void setImageCoins(int imageCoins) {
        ImageCoins = imageCoins;
    }

    public int getVideoCoins() {
        return VideoCoins;
    }

    public void setVideoCoins(int videoCoins) {
        VideoCoins = videoCoins;
    }

    public int getLiveCallCoins() {
        return LiveCallCoins;
    }

    public void setLiveCallCoins(int liveCallCoins) {
        LiveCallCoins = liveCallCoins;
    }
}
